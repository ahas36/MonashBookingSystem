    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Date startdate = null;
        Date endDate = null;
        try {
            startdate = (sdf.parse(txtStartDate1.getText().trim() + " " + txtStartTime1.getText().trim()));
            endDate = (sdf.parse(txtEndDate1.getText().trim() + " " + txtEndTime1.getText().trim()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "The date format should be like 2016-12-28 and time format should be like 21:22",
                    "Worng date time format ",
                    JOptionPane.ERROR_MESSAGE);
        }
        if (endDate.compareTo(startdate) < 0) {
            JOptionPane.showMessageDialog(this,
                    "The date start date should be before end date",
                    "Worng date ",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        List<? extends IFacility> facilites;
        switch (jComboBox1.getSelectedItem().toString()) {
            case "All":
                facilites=fbs.findAllAvailableFacilitys(startdate, endDate);
                break;
            case "Room":
                facilites=fbs.findAllAvailableRooms(startdate, endDate);
                break;
            default:
                facilites=fbs.findAllAvailableEquipments(startdate, endDate);

        }
        DefaultTableModel dtm= (DefaultTableModel) jTable1.getModel();
        int rowCount = dtm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dtm.removeRow(i);
        }
        for (IFacility facilite : facilites) {
            Object[] obj;
            if(facilite instanceof Facility)
            {
                Facility f=(Facility) facilite;
                obj=new Object[]{f.getFacilityId(),f.getFacilityName(),f.getBookingDurationUnit(),f.getMaxBooking(),"fee: "+f.getFacilityFee()};
            }
            else if(facilite instanceof Room)
            {
                Room r=(Room) facilite;
                obj=new Object[]{r.getFacility().getFacilityId(),r.getFacility().getFacilityName(),r.getFacility().getBookingDurationUnit(),r.getFacility().getMaxBooking(),
                    "fee: "+r.getFacility().getFacilityFee()+"\n"+" capacity: "+r.getRoomCapacity()
                };

            }
            else
            {
                 Equipment r=(Equipment) facilite;
                obj=new Object[]{r.getFacility().getFacilityId(),r.getFacility().getFacilityName(),r.getFacility().getBookingDurationUnit(),r.getFacility().getMaxBooking(),
                    "fee: "+r.getFacility().getFacilityFee()+"\n"+" capacity: "+r.getEquipmentBoughtDate()
                };
            }
            dtm.addRow(obj);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

