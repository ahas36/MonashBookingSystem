    private void btnAddSBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSBActionPerformed
        SubBooking sb = new SubBooking();
        try {
            sb.setStartDate(sdf.parse(txtStartDate.getText().trim() + " " + txtStartTime.getText().trim()));
            sb.setEndDate(sdf.parse(txtEndDate.getText().trim() + " " + txtEndTime.getText().trim()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "The date format should be like 2016-12-28 and time format should be like 21:22",
                    "Worng date time format ",
                    JOptionPane.ERROR_MESSAGE);
        }
        if (sb.getEndDate().compareTo(sb.getStartDate()) < 0) {
            JOptionPane.showMessageDialog(this,
                    "The date start date should be before end date",
                    "Worng date ",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (facilityName.getSelectedItem() instanceof Room) {
            sb.setFacilityId(((Room) facilityName.getSelectedItem()).getFacility());
        } else {
            sb.setFacilityId(((Equipment) facilityName.getSelectedItem()).getFacility());
        }

        sb.setSubBookingSatuts("active");
        String temp = fbs.addSubBooking(booking2Create, sb);
        if (temp.isEmpty()) {
            ((DefaultTableModel) tblSubBookingList.getModel()).addRow(new Object[]{sb.getFacilityId().getFacilityName(), sb.getStartDate(), sb.getEndDate(), sb.getFacilityId().getFacilityFee()});
        } else {
            JOptionPane.showMessageDialog(this,
                    temp,
                    "Worng date",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }


    }//GEN-LAST:event_btnAddSBActionPerformed

